def _get_problems_lazy() -> list[dict]:
    """Module-level getter — defers heavy load until first call."""
    return _maybe_get_problems()


# Backwards-compatible name used by codegen_base.py
class _PROBLEMS_LIST:
    """Lazy proxy: behaves like a list but loads on first access."""

    _cache: Optional[list[dict]] = None

    def _materialize(self) -> list[dict]:
        if self._cache is None:
            self._cache = _get_problems_lazy()
        return self._cache

    def __len__(self) -> int:
        return len(self._materialize())

    def __getitem__(self, i):
        return self._materialize()[i]

    def __iter__(self):
        return iter(self._materialize())


PROBLEMS = _PROBLEMS_LIST()


def get_problem(seed: int) -> dict:
    """Pick a problem deterministically from seed."""
    return PROBLEMS[seed % len(PROBLEMS)]
