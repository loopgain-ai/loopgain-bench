"""W1 code-gen corpus: MBPP+ (Liu et al., NeurIPS 2023).

Per BENCH_PROTOCOL.md Amendment 2026-05-21d (committed pre-Phase-2-data), W1
uses MBPP+ (default fallback after HumanEval+ failed density check on Haiku
4.5 — the early HumanEval+ problems one-shot at 8/8 in stage-gate). MBPP+
is the published, peer-reviewed extension of MBPP with extended +tests.
Source: Liu et al., "Is Your Code Generated by ChatGPT Really Correct?"
(NeurIPS 2023). Target density: 15-30% iter-1 failure rate at n=10.

MBPP+ prompts are docstring-only (no def line); the LLM must infer the
signature from the assertion example included in the docstring. The
entry_point name is in the dataset, separate from the prompt — the bench
appends it to the prompt as guidance.

Each problem yields:
  - name = task_id (e.g. "HumanEval/47")
  - prompt = the function-signature-plus-docstring the LLM is shown
  - entry_point = function name to extract from the LLM's code
  - tests = a list of `<entry_point>(...) == <expected>` assertion strings
            combining the base test suite (~7 asserts) with a deterministic
            sample of HumanEval+ extended inputs (8 more asserts; expected
            values precomputed from the canonical solution).

The dataset is loaded once at module-import time via evalplus's `get_human_
eval_plus`. Cache path: `~/Library/Caches/evalplus/HumanEvalPlus-v0.1.10.jsonl`.

Per the protocol's reproducibility lockdowns, we pin to `HUMANEVAL_PLUS_
VERSION = v0.1.10` and load deterministically. The plus-input sample is
seeded so the same problem produces the same test list across trials.
"""

from __future__ import annotations

import os
import random
import re
from functools import lru_cache
from typing import Optional

# evalplus pins MBPP_PLUS_VERSION; the dataset is the registered v0.2.0 release.
MBPP_PLUS_VERSION = "v0.2.0"

# How many extended-input assertions to add per problem (in addition to the
# base tests parsed from the `test` field). Locked here for reproducibility.
N_PLUS_SAMPLES = 8


def _parse_base_tests(test_field: str) -> list[str]:
    """Extract `assert candidate(...) == ...` lines from the HumanEval+ test field.

    Each line is rewritten to use the actual entry_point name (handled by the
    caller via str.format with `candidate={entry_point}`). We return them with
    the literal token `candidate` still in place so a downstream str.replace
    swaps in the workload's entry point.
    """
    lines = test_field.split("\n")
    out: list[str] = []
    for line in lines:
        stripped = line.strip()
        if stripped.startswith("assert candidate"):
            # Drop the leading `assert ` so the bench can wrap each as a
            # boolean expr (the _run_tests harness expects a Python expression
            # that evaluates to True/False, not a statement).
            out.append(stripped[len("assert "):])
    return out


def _safe_repr(value) -> str:
    """Stable, eval-safe repr of values returned by HumanEval canonical solutions.

    Floats are rendered with !r (full precision). Tuples/lists/dicts recurse.
    """
    return repr(value)


def _exec_canonical_solution(problem: dict) -> dict:
    """Exec the problem's canonical solution and return its sandbox dict.

    The canonical_solution field is the body of the function; we paste it
    after the prompt (which has the signature) and exec the combined source.
    Returns the sandbox so callers can pull the entry_point function.
    """
    src = problem["prompt"] + problem["canonical_solution"]
    sandbox: dict = {}
    exec(src, sandbox)
    return sandbox


def _make_plus_assertions(
    problem: dict, *, n_samples: int, seed: int
) -> list[str]:
    """Sample n_samples from plus_input, run canonical to get expected outputs,
    return assertion strings of the form `candidate(*args) == <expected_repr>`.

    The sample is seeded so the same problem produces the same assertions
    across calls — important for reproducibility (lockdown #4: same seeds
    across conditions).
    """
    plus_input = problem.get("plus_input") or []
    if not plus_input:
        return []
    canon_sandbox = _exec_canonical_solution(problem)
    canon_fn = canon_sandbox[problem["entry_point"]]

    rng = random.Random(seed)
    n_avail = len(plus_input)
    idxs = sorted(rng.sample(range(n_avail), min(n_samples, n_avail)))

    asserts: list[str] = []
    for i in idxs:
        args = plus_input[i]
        try:
            expected = canon_fn(*args)
        except Exception:
            # If the canonical solution itself fails on a plus input, skip.
            continue
        args_repr = ", ".join(_safe_repr(a) for a in args)
        exp_repr = _safe_repr(expected)
        asserts.append(f"candidate({args_repr}) == {exp_repr}")
    return asserts


def _make_base_assertions_from_inputs(
    problem: dict, *, n_max: int
) -> list[str]:
    """Generate assertions from base_input by running canonical_solution.

    Used when the dataset (MBPP+) doesn't include a parsable `test` field —
    we instead synthesize one `entry_point(*args) == <expected>` line per
    base_input row.
    """
    base_input = problem.get("base_input") or []
    if not base_input:
        return []
    canon_sandbox = _exec_canonical_solution(problem)
    canon_fn = canon_sandbox[problem["entry_point"]]
    asserts: list[str] = []
    for i, args in enumerate(base_input[:n_max]):
        try:
            expected = canon_fn(*args)
        except Exception:
            continue
        args_repr = ", ".join(_safe_repr(a) for a in args)
        asserts.append(f"candidate({args_repr}) == {_safe_repr(expected)}")
    return asserts


def _to_problem_record(task_id: str, problem: dict) -> dict:
    """Convert one HumanEval+ OR MBPP+ entry into the bench's problem-record format.

    Detects the dataset shape by field presence:
      - HumanEval+ has a parseable `test` field (assert candidate(...) lines)
      - MBPP+ does NOT have `test`; uses `base_input` + `plus_input` instead

    Filters out any generated assertion the canonical solution can't itself
    pass — that ensures the bench's quality target is canonical-achievable
    (no problem has an unwinnable test), so the model is judged against a
    fair bar.
    """
    entry_point = problem["entry_point"]
    seed = abs(hash(task_id)) % (2**31)
    if problem.get("test"):
        base_asserts = _parse_base_tests(problem["test"])
    else:
        base_asserts = _make_base_assertions_from_inputs(problem, n_max=8)
    plus_asserts = _make_plus_assertions(problem, n_samples=N_PLUS_SAMPLES, seed=seed)
    raw_tests = [a.replace("candidate(", f"{entry_point}(") for a in base_asserts + plus_asserts]

    canon_sandbox = _exec_canonical_solution(problem)
    kept: list[str] = []
    for a in raw_tests:
        try:
            if bool(eval(a, canon_sandbox)):
                kept.append(a)
        except Exception:
            continue
    return {
        "name": task_id,
        "prompt": problem["prompt"],
        "entry_point": entry_point,
        "tests": kept,
    }


@lru_cache(maxsize=1)
def _load_problems() -> list[dict]:
    """Load MBPP+ problems once. Cached for process lifetime.

    Some MBPP+ problems produce very large integer outputs (factorial-style).
    Python 3.11+ raises ValueError on int->str above the default 4300-digit
    limit. We raise the cap before processing so the canonical-solution
    evaluator can render them.
    """
    import sys as _sys
    try:
        _sys.set_int_max_str_digits(1_000_000)
    except AttributeError:
        pass  # Python <3.11

    from evalplus.data import get_mbpp_plus

    data = get_mbpp_plus()
    sorted_ids = sorted(data.keys(), key=lambda s: int(s.split("/")[1]))
    records: list[dict] = []
    for tid in sorted_ids:
        try:
            rec = _to_problem_record(tid, data[tid])
            if rec["tests"]:  # drop problems with no surviving tests
                records.append(rec)
        except Exception as exc:
            import sys as _s
            _s.stderr.write(f"  [codegen] skipping {tid}: {exc!r}\n")
            continue
    return records


def _maybe_get_problems() -> list[dict]:
    """Load problems, but tolerate evalplus being unavailable in test contexts.

    Returns a tiny fallback corpus in mock mode or if evalplus can't be loaded
    (e.g. CI without the cache primed). The fallback intentionally mirrors the
    earlier toy-problem shape so the harness still smoke-tests.
    """
    if os.environ.get("BENCH_MOCK") == "1" and os.environ.get("BENCH_USE_REAL_CORPUS") != "1":
        return _FALLBACK_PROBLEMS
    try:
        return _load_problems()
    except Exception as exc:
        # Surface, but don't crash the import — return fallback so unit tests run.
        import sys
        sys.stderr.write(f"[codegen_problems] failed to load HumanEval+: {exc!r}\n")
        sys.stderr.write(f"[codegen_problems] falling back to toy problems (mock-only OK)\n")
        return _FALLBACK_PROBLEMS


